#---------------------------------------------------------------------

sub new {
	my($caller, %args) = @_;
	my $class = ref($caller) || $caller;
	my $self = {};
	unless($args{user_agent}) {
		$args{user_agent} = $ENV{HTTP_USER_AGENT};
	}
	unless($args{remote_addr}) {
		$args{remote_addr} = $ENV{REMOTE_ADDR};
	}
	$self->{user_agent} = $args{user_agent};
	$self->{remote_addr} = $args{remote_addr};
	$self->{ip_detect_mode} = $args{ip_detect_mode};
	bless $self, $class;
	return $self;
}


sub user_agent {
	my($self) = @_;
	return $self->{user_agent};
}

sub carrier {
	my($self, $ua) = @_;
	if($self->{ip_detect_mode}) {
		return $self->_detect_carrier_by_ip($ua);
	} else {
		return $self->_detect_carrier($ua);
	}
}


#---------------------------------------------------------------------

sub _detect_carrier {
	my($self, $ua) = @_;
	unless($ua) {
		$ua = $self->{user_agent};
	}
	if($ua =~ /DoCoMo/) {
		return 'DoCoMo';
	} elsif($ua =~ /KDDI/ || $ua =~ /UP\.\s*Browser/) {
		return 'KDDI';
	} elsif($ua =~ /(J\-PHONE|Vodafone|Softbank)/) {
		return 'Softbank';
	} elsif($ua =~ /(DIPOCKET|WILLCOM)\;\w+\/([\w\d\-]+)/i) {
		return 'WILLCOM';
	} elsif($ua =~ /(KYOCERA|SHARP)\/([AW][\w\d\-]+)/) {
		return 'WILLCOM';
	} elsif($ua =~ /KYOCERAAH\-([\w\d\-]+)/) {
		return 'WILLCOM';
	} elsif($ua =~ /SHARP ([AW][\w\d\-]+)\//) {
		return 'WILLCOM';
	} else {
		return '';
	}
}

sub _detect_carrier_by_ip {
	my($self, $ip) = @_;
	unless($ip) {
		$ip = $self->{remote_addr};
	}
	my $type = '';
	my @parts = split(/\./, $ip);
	my $bitptn;
	for my $decimal (@parts) {
		$bitptn .= unpack("B8", pack("C", $decimal));
	}
	while( my($c, $ref) = each %mobi_subnets ) {
		for my $subnet (@{$ref}) {
			if( $subnet =~ /^(\d+)\.(\d+)\.(\d+)\.(\d+)\/(\d+)$/ ) {
				my @parts;
				$parts[1] = $1;
				$parts[2] = $2;
				$parts[3] = $3;
				$parts[4] = $4;
				my $mask = $5;
				my $bits;
				for(my $i=1; $i<=4; $i++) {
					$bits .= unpack("B8", pack("C", $parts[$i]));
				}
				my $maskptn = substr($bits, 0, $mask);
				if($bitptn =~ /^${maskptn}/) {
					$type = $c;
					last;
				}
			}
		}
		if($type) {last;}
	}
	return $type;
}


